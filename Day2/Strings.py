#Modify strings using built-in methods

#.replace() method
word = "Hello, World!"
print(word)
new_word = word.replace("Hello, World!", "Hi, World!")
print(new_word)

#.lower() method
artist = "JOHN"
print(artist)
lower_artist = artist.lower()
print(lower_artist)

#.upper() method
actor = "joe"
print(actor)
upper_actor = actor.upper()
print(upper_actor)

#.strip() method, removes any whitespace
word = " Hello, World!"
print(word.strip())