#max() function
numbers = [455,1000,606,900]
print(max(numbers))

#min() function
numbers = [455,1000,606,900]
print(min(numbers))

#sum() function
numbers = [455,1000,606,900]
print(sum(numbers))

#combination of sum() and len() function
data = [3,5,6,8,9,10]

average = sum(data) / len(data)
print(average)

#rounded() function takes two argument (number, ndigits)
rounded_average = round(average, 2)
print(rounded_average)

#sorted() function
cooking_times = [30, 45, 60, 10]
cooking_times_sorted = sorted(cooking_times)
print(cooking_times_sorted)